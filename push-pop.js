// (push , pop)..

// push = add value at the last position

// push
var nmb = [20, 40, 60, 80, 100]
nmb.push(120)
console.log(nmb)
// ans : [ 20, 40, 60, 80, 100, 120 ]

var friends = ["sabit", "susan", "isa"];
friends.push("aqib")
console.log(friends)
// ans : [ 'sabit', 'susan', 'isa', 'aqib' ]

// pop = terminate a value from the last position

// pop
var nmb = [20, 40, 60, 80, 100]
nmb.pop()
console.log(nmb)
// ans : [ 20, 40, 60, 80 ]

var friends = ["sabit", "susan", "isa"];
friends.pop()
console.log(friends)
// ans : [ 'sabit', 'susan' ]
